import { Injectable } from "@nestjs/common";
import type { Player } from "@monopoly/shared";

/**
 * Обработчик тюремной механики.
 *
 * Правила Монополии (по официальной спецификации):
 *
 *  - Попасть в тюрьму можно ТРЕМЯ путями:
 *      1) Остановиться на клетке «В тюрьму» (GOTO_JAIL, id=30).
 *      2) Вытянуть карточку «Идёшь в тюрьму» (Chance / Treasury).
 *      3) Выбросить три дубля подряд.
 *    Во всех трёх случаях фишка МГНОВЕННО переносится на клетку JAIL
 *    (id=10), `inJail=true`. Фишка НЕ двигается по часовой стрелке —
 *    это телепорт, а не «шаг».
 *
 *  - В ТЕКУЩЕМ ходу (том, в котором попал в тюрьму) игроку разрешено
 *    ТОЛЬКО завершить ход. Никаких «бросить кубики» / «купить» /
 *    «торговать» — он в тюрьме.
 *
 *  - В СЛЕДУЮЩЕМ своём ходу у игрока появляется модальное окно тюрьмы
 *    (фаза `JAIL_DECISION`), где он может:
 *      a) Использовать карточку «Выход из тюрьмы бесплатно»
 *         (если есть);
 *      b) Заплатить ₽50 штрафа;
 *      c) Попробовать выбросить дубль.
 *    Если выпал дубль — игрок выходит и ходит как обычно.
 *    Если не выпал — ход передаётся.
 *
 *  - После 3 ходов в тюрьме без дубля — принудительно списывается
 *    ₽50 и игрок выходит (правило «3 попытки»).
 *
 *  - Пока игрок в тюрьме, `mustRollAgain` сбрасывается, `jailTurns=0`,
 *    `consecutiveDoubles=0` — это конец любой «серии дублей».
 */
@Injectable()
export class JailHandlerService {
  /**
   * Отправить игрока в тюрьму (любой из трёх путей выше).
   *
   * Сбрасывает `consecutiveDoubles` (правило трёх дублей),
   * `mustRollAgain` (если был флаг повторного броска — он теряет силу,
   * потому что игрок теперь в тюрьме и в этом ходу бросать уже нельзя).
   *
   * ВАЖНО: эта функция только МУТИРУЕТ игрока и НЕ меняет `state.phase`.
   * Решение о следующей фазе принимает вызывающий код (GamesService):
   *  - при попадании через GOTO_JAIL / карту / 3 дубля → JAIL_DECISION
   *    (т.е. в ЭТОМ же ходу модалка тюрьмы не показывается, игрок
   *    только нажимает «Завершить ход»);
   *  - при `START_TURN` следующего хода (если ещё в тюрьме) →
   *    JAIL_DECISION (тут уже модалка с тремя способами выхода).
   */
  sendToJail(player: Player): void {
    player.position = 10; // позиция клетки JAIL на стандартной доске
    player.inJail = true;
    player.jailTurns = 0;
    player.consecutiveDoubles = 0;
    // Сбрасываем «обязан бросить ещё раз» — в тюрьме это правило
    // не действует, в текущем ходу бросок недопустим.
    player.mustRollAgain = false;
  }

  /**
   * Попытка выйти из тюрьмы броском кубиков.
   *
   * Возвращает один из трёх исходов:
   *  - `"escape"` — игрок выбросил дубль и сразу вышел;
   *  - `"stay"`   — остаётся в тюрьме ещё на один ход;
   *  - `"pay"`    — третий ход без дубля, принудительно списали ₽50.
   *
   * ВАЖНО: метод сам НЕ меняет `position` — это сделает вызывающий код
   * в `handleJailDecision`, когда `outcome === "escape"|"pay"`.
   * Также метод НЕ переводит фазу — это тоже забота GamesService.
   */
  tryDoubleOrPay(player: Player, dice: [number, number]): "escape" | "stay" | "pay" {
    // Дубль — счастливый билет на выход.
    if (dice[0] === dice[1]) {
      player.inJail = false;
      player.jailTurns = 0;
      return "escape";
    }
    // Не дубль — считаем попытки.
    player.jailTurns += 1;
    if (player.jailTurns >= 3) {
      // Принудительная оплата штрафа. `Math.max(0, ...)` — защита от
      // отрицательного баланса; в реальной логике после этого должен
      // сработать `BankruptcyService` (TODO Шаг 27+).
      player.money = Math.max(0, player.money - 50);
      player.inJail = false;
      player.jailTurns = 0;
      return "pay";
    }
    return "stay";
  }
}
