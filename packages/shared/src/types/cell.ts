// CellType — перечисление всех возможных типов клеток
export type CellType =
  | "GO" // Старт (Вперёд)
  | "PROPERTY" // Обычная собственность (можно купить)
  | "RAILROAD" // Железная дорога (4 штуки)
  | "UTILITY" // Коммунальное предприятие (Электричество, Вода)
  | "CHANCE" // Карточка Шанс
  | "TREASURY" // Общественная казна
  | "TAX" // Налог
  | "JAIL" // Тюрьма (просто визит)
  | "GOTO_JAIL" // Клетка "В тюрьму!" (отправляет в тюрьму)
  | "PARKING"; // Бесплатная стоянка

// PropertyGroup — группы собственности по цветам
// Когда собираешь ВСЮ группу — получаешь монополию (рента x2)
export type PropertyGroup =
  | "brown" // Коричневые (2 шт: Старая дорога, Набережная)
  | "lightblue" // Голубые (3 шт)
  | "pink" // Розовые (3 шт)
  | "orange" // Оранжевые (3 шт)
  | "red" // Красные (3 шт)
  | "yellow" // Жёлтые (3 шт)
  | "green" // Зелёные (3 шт)
  | "darkblue" // Тёмно-синие (2 шт: Арбат, Пречистенка)
  | "railroad" // Железные дороги (4 шт, особый тип)
  | "utility"; // Предприятия (2 шт: Электричество, Вода)

// Cell — основной интерфейс клетки
export interface Cell {
  // Уникальный ID (0-39)
  id: number;

  // Название для отображения
  name: string;

  // Тип клетки (из CellType выше)
  type: CellType;

  // Группа по цвету (только для PROPERTY, RAILROAD, UTILITY)
  group?: PropertyGroup;

  // HEX-цвет группы (для цветной полоски сверху клетки)
  color?: string;

  // Цена покупки в рублях
  // ? — undefined для клеток, которые нельзя купить (GO, CHANCE, TAX)
  price?: number;

  // Базовая рента (без домов)
  rent?: number;

  // Таблица ренты: [base, 1дом, 2дома, 3дома, 4дома, отель]
  // Используется когда на клетке есть дома
  rentTable?: number[];

  // Цена одного дома для этой группы
  housePrice?: number;

  // Сумма, которую получишь при залоге (обычно price / 2)
  mortgageValue?: number;

  // Сумма налога (только для TAX)
  taxAmount?: number;

  // Иконка
  icon?: string;

  // ID владельца (string из типа Player.id)
  // undefined = клетка принадлежит банку
  ownerId?: string;

  // Количество домов (0-4 = дома, 5 = отель)
  houses: 0 | 1 | 2 | 3 | 4 | 5;

  // Заложена ли клетка
  // Заложенная клетка не приносит ренту
  isMortgaged: boolean;
}
