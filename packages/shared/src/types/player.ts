// PlayerKind — тип игрока
// human — реальный человек (управляется через UI)
// bot — компьютерный противник (управляется через AI)
// guest — без регистрации
export type PlayerKind = "human" | "bot" | "guest";

// Player — основной интерфейс игрока
export interface Player {
  // Уникальный ID (uuid, генерируем на сервере)
  id: string;

  // Имя для отображения
  displayName: string;

  // Тип игрока (из PlayerKind)
  kind: PlayerKind;

  // HEX-цвет фишки
  // Используется для подсветки и идентификации на доске
  color: string;

  // Иконка
  icon: string;

  // Текущий баланс в рублях
  money: number;

  // Позиция на доске (0-39)
  position: number;

  // Находится ли в тюрьме
  inJail: boolean;

  // Сколько ходов уже сидит в тюрьме (0-3)
  // После 3 ходов — вынужден заплатить 50₽
  jailTurns: number;

  // Сколько карточек "выйди из тюрьмы бесплатно" у игрока
  jailCards: number;

  // Массив ID клеток, принадлежащих игроку
  properties: number[];

  // Счётчик подряд выпавших дублей (для правила "3 дубля = тюрьма")
  // Сбрасывается в 0 при ходе без дубля или при попадании в тюрьму.
  // Обязательное поле — используется игровой логикой на сервере и клиенте.
  consecutiveDoubles: number;

  // Обанкротился ли игрок
  // После банкротства удаляется из ротации
  isBankrupt: boolean;

  /**
   * флаг «после текущего хода нужно бросить ещё раз» (правило дубля).
   * Выставляется в `ROLL_DICE` при выпадении дубля (1-й или 2-й подряд),
   * сбрасывается при `nextPlayer` или при бросе без дубля. Пока `true`,
   * после покупки/отказа/события фаза переходит в `ROLLING` (а не в
   * END_TURN), и тот же игрок бросает кубики повторно.
   */
  mustRollAgain?: boolean;
}
